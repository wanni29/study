package practice;

public class Practice5 {
    public static void main(String[] args) {
        // 코드 삽질
        // for구문으로 별 1개
        for (int i = 0; i < 1; i++) {
            System.out.print("*");
        }
        System.out.println();

        // for구문으로 별 2개
        for (int i = 0; i < 2; i++) { // 0, 1
            System.out.print("*");
        }
        System.out.println();

        // for구문으로 별 3개
        for (int i = 0; i < 3; i++) { // 0, 1, 2
            System.out.print("*");
        }
        System.out.println();

        // for구문으로 별 4개
        for (int i = 0; i < 4; i++) { // 0, 1, 2, 3
            System.out.print("*");

        }
        System.out.println();

        // for구문으로 별 5개
        for (int i = 0; i < 5; i++) { // 0, 1, 2, 3, 4
            System.out.print("*");

        }
        System.out.println();
    }
}
